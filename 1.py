# To find the sum of square root of any three numbers.

import math

n1=int(input("Enter 1 number :"))
n2=int(input("Enter 2 number :"))
n3=int(input("Enter 3 number :"))

sq1=(math.sqrt(n1))
sq2=(math.sqrt(n2))
sq3=(math.sqrt(n3))

sum=sq1+sq2+sq3

print("Sum : ",sum)

# Output:
# Enter 1 number :9
# Enter 2 number :25
# Enter 3 number :16
# Sum :  12.0