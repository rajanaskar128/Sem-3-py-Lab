# Define a sum function with two parameters and call the function

def sum(n1, n2):
    return n1 + n2

n1 = int(input("Enter 1 number: "))
n2 = int(input("Enter 2 number: "))

result = sum(n1, n2)

print(f"The sum of {n1} and {n2} is: {result}")

# Output 

# Enter 1 number: 5
# Enter 2 number: 3
# The sum of 5 and 3 is: 8